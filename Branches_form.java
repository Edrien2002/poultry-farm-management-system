    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
            String s1 = txt_ID.getText();   
            String s2 = txtLocation.getText();
            String s3 = txtNumber_of_poultry_houses.getText();
            
            /*checking if there is data entered in the ID field*/
            if (!s1.isEmpty()) {
                JOptionPane.showMessageDialog(this,"The ID field must be empty as it is auto-generated. Please clear the ID field.","Invalid ID Input",JOptionPane.ERROR_MESSAGE);
                txt_ID.requestFocus();
                txt_ID.selectAll();
                return;
            }
            
            /*checking for empty fields*/
            if(s2.isEmpty() || s3.isEmpty()){
                JOptionPane.showMessageDialog(this,"Please fill all required fields","Validation Error",JOptionPane.ERROR_MESSAGE);
                if(s2.isEmpty()){
                    txtLocation.requestFocus();
                }
                else{
                    txtNumber_of_poultry_houses.requestFocus();
                }
                return;
            }
            
            /*check number of poultry houses is positive*/
            if(!s3.matches("\\d+")){
                JOptionPane.showMessageDialog(this,"Number of poultry houses must be a positive whole number", "Invalid Data Type",JOptionPane.ERROR_MESSAGE);
                txtNumber_of_poultry_houses.requestFocus();
                txtNumber_of_poultry_houses.selectAll();
                return;
            }
            int houses = Integer.parseInt(s3);
            if(houses < 0){
                JOptionPane.showMessageDialog(this,"Number of poultry houses cannot be negative","Invalid Value",JOptionPane.ERROR_MESSAGE);
                txtNumber_of_poultry_houses.requestFocus();
                txtNumber_of_poultry_houses.selectAll();
                return;
            }
            
            /*checking location characters*/
            if(!s2.matches("^[a-zA-Z\\s\\-\\'\\,]+$")){
                JOptionPane.showMessageDialog(this, "Location cannot contain integers","Invalid Location Format", 
                JOptionPane.ERROR_MESSAGE);
                txtLocation.requestFocus();
                txtLocation.selectAll();
                return;
            }
            
            /*checking location length*/
            if(s2.length() < 5){
                JOptionPane.showMessageDialog(this,"Location name is too short","Invalid Location",JOptionPane.ERROR_MESSAGE);
                txtLocation.requestFocus();
                txtLocation.selectAll();
                return;
            }
            if(s2.length() > 20){
               JOptionPane.showMessageDialog(this,"Location name is too long (max 20 characters)","Invalid Location",JOptionPane.ERROR_MESSAGE);
                txtLocation.requestFocus();
                txtLocation.selectAll();
                return; 
            }
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                con = DriverManager.getConnection(cs, user, password);
                st = con.createStatement();
                String validate_query = "SELECT Location FROM branches WHERE LOWER(Location) = LOWER(?)";
                PreparedStatement checkStmt = con.prepareStatement(validate_query);
                checkStmt.setString(1, s2);
                rs = checkStmt.executeQuery();
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this,"Branch already exists.","Duplicate Location Warning",JOptionPane.ERROR_MESSAGE);
                    txtLocation.requestFocus();
                    txtLocation.selectAll();
                    return;
                }
                
                query = "INSERT INTO branches (Location, Number_of_poultry_houses) VALUES ( '" + s2 + "', '" + s3 + "')";
                /*validate saving*/
                int confirm = JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to Save this Branch","Confirm Save", 
                JOptionPane.YES_NO_OPTION);
        
                if (confirm != JOptionPane.YES_OPTION) {
                    return; // User cancelled1
                }
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(null,"Record Saved Successfully");
                txt_ID.setText("");
                txtLocation.setText("");
                txtNumber_of_poultry_houses.setText("");
                
                txtLocation.requestFocus();
}
          
          catch (SQLException ex){
              ex.printStackTrace();
}
          catch (ClassNotFoundException e){
              e.printStackTrace();
}
          finally {
                try{
                    if (st != null){
                        st.close();
                    }
                    if (con != null){
                        con.close();
                    }
                }
                catch (SQLException ex){
                    ex.printStackTrace();
                }
}
    }//GEN-LAST:event_btnSaveActionPerformed

