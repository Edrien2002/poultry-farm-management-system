     private void populatepoultryid() {
         Connection localCon = null;
        Statement localSt = null;
        ResultSet localRs = null;
         
        try {
           Class.forName("com.mysql.cj.jdbc.Driver");
            localCon = DriverManager.getConnection(cs, user, password);
            localSt = localCon.createStatement();
            query = "SELECT ID FROM poultry";
        localRs = localSt.executeQuery(query);
        
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            model.addElement("--Select Poultry ID---");
            while (localRs.next()) {
                model.addElement(localRs.getString("ID"));
            }
            comboPoultry_id.setModel(model);
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Error loading : " + ex.getMessage(), 
                "Database Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        } finally {
            try {
                if (localRs != null) localRs.close();
                if (localSt != null) localSt.close();
                if (localCon != null) localCon.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }

