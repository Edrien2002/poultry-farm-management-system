    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       String s1 = txt_ID.getText();
	if (s1.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please first find the record in the branches to delete", "Error", JOptionPane.ERROR_MESSAGE);
        txt_ID.requestFocus();
        return;
    }
        
			
	try {
            Class.forName("com.mysql.cj.jdbc.Driver"); // register the driver,
            con = DriverManager.getConnection(cs, user, password);
            String deletequery = "DELETE FROM health WHERE ID = ?";
            PreparedStatement deleteStmt = con.prepareStatement(deletequery);
            deleteStmt.setString(1, s1);
            int confirm = JOptionPane.showConfirmDialog(this, "Are you Sure You want to Delete record with ID: " + s1 + "?", "Confirm Delete", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (confirm != JOptionPane.YES_OPTION){
                return; //user cancelled
            }
            else{
                int rowsAffected = deleteStmt.executeUpdate();
             if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Record deleted successfully!");
                    // Clear fields after successful deletion
                    clearFields();
                }
            
            else {
                    JOptionPane.showMessageDialog(this, "No record found with ID: " + s1, "Error", JOptionPane.ERROR_MESSAGE);
            } 
        }
        }
        catch (SQLException ex) 
        {
            ex.printStackTrace();
	} 
        catch (ClassNotFoundException e) 
        {
            e.printStackTrace();
	} 
        finally {

            try {
		if (st != null) 
                {
                    st.close();
		}
		if (con != null) 
                {
                    con.close();
		}

            } 
            catch (SQLException ex) 
            {
                ex.printStackTrace();
            }
	}
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
            String s1 = txt_ID.getText();   
            String s2 = txtDate.getText();
            String s3 = (String) comboPoultry_id.getSelectedItem();
            String s4 = txtDiagnosis.getText();
            String s5 = txtTreatment.getText();
            String s6 = txtVaccine.getText();
            
            
            /*checking if there is data entered in the ID field*/
            if (!s1.isEmpty() || !s2.isEmpty()) {
                JOptionPane.showMessageDialog(this,"This field must be empty as it is auto-generated. Please clear this field.","Invalid ID Input",JOptionPane.ERROR_MESSAGE);
                if(!s1.isEmpty()){
                txt_ID.requestFocus(); 
                txt_ID.selectAll();
                }
                else{
                    txtDate.requestFocus(); 
                    txtDate.selectAll();
                }
                return;
            }
            
            /*checking for empty fields*/
            if(s3.equals("--Select Poultry ID---") || s4.isEmpty() || s5.isEmpty() || s6.isEmpty()){
                JOptionPane.showMessageDialog(this,"Please fill all required fields","Validation Error",JOptionPane.ERROR_MESSAGE);
                if(s3.equals("--Select Poultry ID---")){
                    comboPoultry_id.requestFocus();
                }
               
                else if(s4.isEmpty()){
                    txtDiagnosis.requestFocus();
                }
                else if(s5.isEmpty()){
                    txtTreatment.requestFocus();
                }
                else if(s6.isEmpty()){
                    txtVaccine.requestFocus();
                }
               
                return;
            }
            
            /*check number is positive*/
            
            if (s4.length() > 40) {
                JOptionPane.showMessageDialog(this, "Diagnosis cannot exceed 40 characters.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                txtDiagnosis.requestFocus();
                txtDiagnosis.selectAll();
                return;
            }
            if (s5.length() > 40) {
                JOptionPane.showMessageDialog(this, "Treatment cannot exceed 40 characters.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                txtTreatment.requestFocus();
                txtTreatment.selectAll();
                return;
            }
            if (s6.length() > 40) {
                JOptionPane.showMessageDialog(this, "Vaccine cannot exceed 40 characters.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                txtVaccine.requestFocus();
                txtVaccine.selectAll();
                return;
            }
                
            
            Connection con = null;
            PreparedStatement pstmt = null;
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                con = DriverManager.getConnection(cs, user, password);
               query = "INSERT INTO health (Poultry_ID, Diagnosis, Treatment,  Vaccine) VALUES (?, ?, ?, ?)";
                pstmt = con.prepareStatement(query);
                
                pstmt.setString(1, s3);
                pstmt.setString(2, s4);
                pstmt.setString(3, s5);
                pstmt.setString(4, s6);
                
                /*validate saving*/
                int confirm = JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to Save this record","Confirm Save",JOptionPane.YES_NO_OPTION);
        
                if (confirm != JOptionPane.YES_OPTION) {
                    return; // User cancelled1
                }
                
                int rowsaffected = pstmt.executeUpdate();
                if(rowsaffected > 0){
                    JOptionPane.showMessageDialog(null,"Record Saved Successfully");
                    clearFields();

                    comboPoultry_id.requestFocus();
                }
                else {
                    JOptionPane.showMessageDialog(this, "Failed to save record","Error",JOptionPane.ERROR_MESSAGE);
                }
}
          
          catch (SQLException ex){
              ex.printStackTrace();
}
          catch (ClassNotFoundException e){
              e.printStackTrace();
}
          finally {
                try{
                    if (st != null){
                        st.close();
                    }
                    if (con != null){
                        con.close();
                    }
                }
                catch (SQLException ex){
                    ex.printStackTrace();
                }
}
    }//GEN-LAST:event_btnSaveActionPerformed

