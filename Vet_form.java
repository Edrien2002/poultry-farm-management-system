    private void jMenuItem76ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem76ActionPerformed
        String sql = "SELECT " +
        "b.Location AS Branch_Location, " +
        "YEAR(h.Entry_Date) AS Year, " +
        "MONTH(h.Entry_Date) AS Month, " +
        "COUNT(h.ID) AS Total_Health_Records, " +
        "COUNT(DISTINCT h.Diagnosis) AS Unique_Diagnoses, " +
        "COUNT(DISTINCT h.Treatment) AS Unique_Treatments, " +
        "GROUP_CONCAT(DISTINCT h.Vaccine) AS Vaccines_Administered " +
        "FROM health h " +
        "INNER JOIN poultry_house ph ON h.Poultry_ID = ph.ID " +
        "INNER JOIN branches b ON ph.Branch_ID = b.ID " +
        "WHERE b.Location = 'NAMUMIRA' " +
        "    AND YEAR(h.Entry_Date) = 2026 " +
        "    AND MONTH(h.Entry_Date) = 1 " +
        "GROUP BY b.Location, YEAR(h.Entry_Date), MONTH(h.Entry_Date)";

        ReportGenerator.generateReport("Monthly Health Report for Namumira", sql);

    }//GEN-LAST:event_jMenuItem76ActionPerformed

