    private void populatecombosid() {
        Connection localCon = null;
    Statement localSt = null;
    ResultSet localRs = null;
     
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        localCon = DriverManager.getConnection(cs, user, password);
        localSt = localCon.createStatement();
        
        // Populate Poultry ID combo box
        query = "SELECT ID FROM supplier";
        localRs = localSt.executeQuery(query);
        
        DefaultComboBoxModel<String> poultryModel = new DefaultComboBoxModel<>();
        poultryModel.addElement("--Select Supplier ID--");
        while (localRs.next()) {
            poultryModel.addElement(localRs.getString("ID"));
        }
        comboSupplier_id.setModel(poultryModel);
        
        // Close the first result set
        localRs.close();
        
        // Populate Reported By combo box with User IDs
        query = "SELECT ID FROM branches";
        localRs = localSt.executeQuery(query);
        
        DefaultComboBoxModel<String> userModel = new DefaultComboBoxModel<>();
        userModel.addElement("--Select Branch ID--");
        while (localRs.next()) {
            userModel.addElement(localRs.getString("ID"));
        }
        comboBranch_id.setModel(userModel);
        
    } catch (SQLException | ClassNotFoundException ex) {
        JOptionPane.showMessageDialog(this, "Error loading data: " + ex.getMessage(), 
            "Database Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    } finally {
        try {
            if (localRs != null) localRs.close();
            if (localSt != null) localSt.close();
            if (localCon != null) localCon.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    }

