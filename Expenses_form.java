    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        String s1 = txt_ID.getText();

        //check if ID is entered
        if ( s1.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter the reord ID to search", "Error", JOptionPane.ERROR_MESSAGE);
            txt_ID.requestFocus();
            return;
        }

        /*check if positive integer is entered*/
        if (!s1.matches("\\d+")) {
            JOptionPane.showMessageDialog(this,"ID must be a positive whole number","Invalid ID Format",JOptionPane.ERROR_MESSAGE);
            txt_ID.requestFocus();
            txt_ID.selectAll();
            return;
        }

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection(cs, user, password);
            query = "SELECT * FROM expenses WHERE ID = ?";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1,s1);

            rs = pst.executeQuery();
            if (rs.next()){
                //record found
                String foundID = rs.getString("ID");
                String Supplier_ID = rs.getString("Supplier_ID");
                String Branch_ID = rs.getString("Branch_ID");
                String Type_of_expense = rs.getString("Type_of_expense");
                String Amount = rs.getString("Amount");
                String Entry_Date = rs.getString("Entry_Date");

                //update the text fields
                txt_ID.setText(foundID);
                txtExpense_type.setText(Type_of_expense);
                comboSupplier_id.setSelectedItem(Supplier_ID);
                txtAmount.setText(Amount);
                comboBranch_id.setSelectedItem(Branch_ID);
                txtDate.setText(Entry_Date);
                JOptionPane.showMessageDialog(null,"Record Retrieved Successfully");
            }
            //no record found
            else {
                JOptionPane.showMessageDialog(this, "No record found with ID: " + s1, "Not Found", JOptionPane.WARNING_MESSAGE);
                clearFields();
                comboSupplier_id.requestFocus();
            }

        }
        catch (SQLException ex){
            ex.printStackTrace();
        }
        catch (ClassNotFoundException e){
            e.printStackTrace();
        }
        finally {
            try{
                if (st != null){
                    st.close();
                }
                if (con != null){
                    con.close();
                }
            }
            catch (SQLException ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String s1 = txt_ID.getText();
        if (s1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please first find the record in  to delete", "Error", JOptionPane.ERROR_MESSAGE);
            txt_ID.requestFocus();
            return;
        }

        try {
            Class.forName("com.mysql.cj.jdbc.Driver"); // register the driver,
            con = DriverManager.getConnection(cs, user, password);
            String deletequery = "DELETE FROM expenses WHERE ID = ?";
            PreparedStatement deleteStmt = con.prepareStatement(deletequery);
            deleteStmt.setString(1, s1);
            int confirm = JOptionPane.showConfirmDialog(this, "Are you Sure You want to Delete Record with ID: " + s1 + "?", "Confirm Delete", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (confirm != JOptionPane.YES_OPTION){
                return; //user cancelled
            }
            else{
                int rowsAffected = deleteStmt.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Record deleted successfully!");
                    // Clear fields after successful deletion
                    clearFields();
                    comboSupplier_id.requestFocus();
                }

                else {
                    JOptionPane.showMessageDialog(this, "No record found with ID: " + s1, "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        catch (SQLException ex)
        {
            ex.printStackTrace();
        }
        catch (ClassNotFoundException e)
        {
            e.printStackTrace();
        }
        finally {

            try {
                if (st != null)
                {
                    st.close();
                }
                if (con != null)
                {
                    con.close();
                }

            }
            catch (SQLException ex)
            {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            String s1 = txt_ID.getText();   
            String s2 = (String) comboSupplier_id.getSelectedItem();
            String s3 = txtExpense_type.getText();
            String s4 = txtAmount.getText();
            String s5 = (String) comboBranch_id.getSelectedItem();
            String s6 = txtDate.getText();
            
            /*checking if there is data entered in the ID field*/
            if (!s1.isEmpty() || !s6.isEmpty()) {
                JOptionPane.showMessageDialog(this,"This field must be empty as it is auto-generated. Please clear this field.","Invalid ID Input",JOptionPane.ERROR_MESSAGE);
                if(!s1.isEmpty()){
                txt_ID.requestFocus(); 
                txt_ID.selectAll();
                }
                else{
                    txtDate.requestFocus(); 
                    txtDate.selectAll(); 
                }
                return;
            }
            
            /*checking for empty fields*/
            if(s2.equals("--Select Supplier ID--") || s3.isEmpty() || s5.equals("--Select Branch ID--") || s4.isEmpty()){
                JOptionPane.showMessageDialog(this,"Please fill all required fields","Validation Error",JOptionPane.ERROR_MESSAGE);
                if(s2.equals("--Select Supplier ID--")){
                    comboSupplier_id.requestFocus();
                }
                else if(s5.equals("--Select Branch ID--")){
                    comboBranch_id.requestFocus();
                }
               
                else if(s3.isEmpty()){
                    txtExpense_type.requestFocus();
                }
                
                else if(s4.isEmpty()){
                    txtAmount.requestFocus();
                }
                
                return;
            }
            
            /*check number is positive*/
         
            if (s3.length() > 15) {
                JOptionPane.showMessageDialog(this, "Expenses Type cannot exceed 15 characters.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                txtExpense_type.requestFocus();
                txtExpense_type.selectAll();
                return;
            }
           
            if(!s3.matches("^[a-zA-Z\\s\\-\\'\\,]+$")){
                JOptionPane.showMessageDialog(this, "Expenses Type cannot contain integers","Invalid Location Format", 
                JOptionPane.ERROR_MESSAGE);
                txtExpense_type.requestFocus();
                txtExpense_type.selectAll();
                return;
            }
    
            Connection con = null;
            PreparedStatement pstmt = null;
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                con = DriverManager.getConnection(cs, user, password);
               query = "INSERT INTO expenses (Supplier_ID, Branch_ID, Type_of_expense, Amount) VALUES (?, ?, ?, ?)";
                pstmt = con.prepareStatement(query);
                
                
                pstmt.setString(1, s2);
                pstmt.setString(2, s5);
                pstmt.setString(3, s3);
                pstmt.setString(4, s4);
               
                /*validate saving*/
                int confirm = JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to Save this record","Confirm Save",JOptionPane.YES_NO_OPTION);
        
                if (confirm != JOptionPane.YES_OPTION) {
                    return; // User cancelled1
                }
                
                int rowsaffected = pstmt.executeUpdate();
                if(rowsaffected > 0){
                    JOptionPane.showMessageDialog(null,"Record Saved Successfully");
                    clearFields();

                    comboSupplier_id.requestFocus();
                }
                else {
                    JOptionPane.showMessageDialog(this, "Failed to save record","Error",JOptionPane.ERROR_MESSAGE);
                }
}
          
          catch (SQLException ex){
              ex.printStackTrace();
}
          catch (ClassNotFoundException e){
              e.printStackTrace();
}
          finally {
                try{
                    if (st != null){
                        st.close();
                    }
                    if (con != null){
                        con.close();
                    }
                }
                catch (SQLException ex){
                    ex.printStackTrace();
                }
}
    }//GEN-LAST:event_jButton1ActionPerformed

