    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String s1 = txtUsername.getText();
        String s2 = new String(txtPassword.getPassword());
         
         //check if Regno. or name are entered
         if ( s1.isEmpty()){
             JOptionPane.showMessageDialog(this, "Username cannot be empty", "Error", JOptionPane.ERROR_MESSAGE);
             txtUsername.requestFocus();
             return;
         }
          if ( s2.isEmpty()){
             JOptionPane.showMessageDialog(this, "Password cannot be empty", "Error", JOptionPane.ERROR_MESSAGE);
             txtPassword.requestFocus();
             return;
         }
            
            
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                con = DriverManager.getConnection(cs, user, password);
                query = "SELECT Role_of_user FROM users WHERE User_Name = ? AND Password_of_user = ?";
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1,s1);
                pst.setString(2,s2);
                
                rs = pst.executeQuery();
                if (rs.next()){
                    String user_role = rs.getString("Role_of_user");
                    JOptionPane.showMessageDialog(null,"Logged in Successfully");
                    navigate_role(user_role);
                    this.dispose();
                }
                //no record found
                else {
                    JOptionPane.showMessageDialog(this, "No account found,ensure you have entered the correct Username and Password", "Not Found", JOptionPane.WARNING_MESSAGE);
                    clearFields();
                    txtUsername.requestFocus();
                }
                
}
          
          catch (SQLException ex){
              ex.printStackTrace();
}
          catch (ClassNotFoundException e){
              e.printStackTrace();
}
          finally {
                try{
                    if (st != null){
                        st.close();
                    }
                    if (con != null){
                        con.close();
                    }
                }
                catch (SQLException ex){
                    ex.printStackTrace();
                }
}
}                                      

        private void clearFields() {
            txtUsername.setText("");
            txtPassword.setText("");

    }//GEN-LAST:event_btnLoginActionPerformed

