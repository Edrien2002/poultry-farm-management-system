    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
            String s1 = txt_ID.getText();   
            String s2 = txtDate.getText();
            String s3 = (String)comboPoultry_house.getSelectedItem();
            String s4 = txtCollection_round.getText();
            String s5 = txtNumber_of_trays.getText();
            String s6 = txtExtra_eggs.getText();
            String s7 = txtTotal.getText();
            
            /*checking if there is data entered in the ID field*/
            if (!s1.isEmpty() || !s2.isEmpty() || !s7.isEmpty()) {
                JOptionPane.showMessageDialog(this,"This field must be empty as it is auto-generated. Please clear this field.","Invalid ID Input",JOptionPane.ERROR_MESSAGE);
                if(!s1.isEmpty()){
                   txt_ID.requestFocus(); 
                   txt_ID.selectAll();
                }
                else if(!s2.isEmpty()){
                    txtDate.requestFocus();
                    txtDate.selectAll();
                 }
                else{
                    txtTotal.requestFocus();
                    txtTotal.selectAll();
                }
                return;
            }
            
            /*checking for empty fields*/
            if(s3 == null|| s4.isEmpty() || s5.isEmpty() || s6.isEmpty() || s3.equals("--Select the poultry house ID---")){
                JOptionPane.showMessageDialog(this,"Please fill all required fields","Validation Error",JOptionPane.ERROR_MESSAGE);
                if(s3 == null){
                    comboPoultry_house.requestFocus();
                }
                else if(s4.isEmpty()){
                    txtCollection_round.requestFocus();
                }
                else if(s5.isEmpty()){
                    txtNumber_of_trays.requestFocus();
                }
                else if(s3.equals("--Select the poultry house ID---")){
                    comboPoultry_house.requestFocus();
                }
                else{
                    txtExtra_eggs.requestFocus();
                }
                return;
            }
            
            /*check number is positive*/
            if(!s4.matches("\\d+")){
                JOptionPane.showMessageDialog(this,"Collection round must be a positive whole number", "Invalid Data Type",JOptionPane.ERROR_MESSAGE);
                txtCollection_round.requestFocus();
                txtCollection_round.selectAll();
                return;
            }
            if (!s5.matches("\\d+")){
                JOptionPane.showMessageDialog(this,"Number of trays must be a positive whole number", "Invalid Data Type",JOptionPane.ERROR_MESSAGE);
                txtNumber_of_trays.requestFocus();
                txtNumber_of_trays.selectAll();
                return;
            }
            if (!s6.matches("\\d+")){
                JOptionPane.showMessageDialog(this,"Number of extra eggs must be a positive whole number", "Invalid Data Type",JOptionPane.ERROR_MESSAGE);
                txtExtra_eggs.requestFocus();
                txtExtra_eggs.selectAll();
                return;
            }
                
            int collection_round = Integer.parseInt(s4);
            int number_of_trays = Integer.parseInt(s5);
            int extra_eggs = Integer.parseInt(s6);
            if(collection_round < 0){
                JOptionPane.showMessageDialog(this,"Collection round cannot be negative","Invalid Value",JOptionPane.ERROR_MESSAGE);
                txtCollection_round.requestFocus();
                txtCollection_round.selectAll();
                return;
            }
            if(number_of_trays < 0){
                JOptionPane.showMessageDialog(this,"Number of trays cannot be negative","Invalid Value",JOptionPane.ERROR_MESSAGE);
                txtNumber_of_trays.requestFocus();
                txtNumber_of_trays.selectAll();
                return;
            }
            if(extra_eggs < 0){
                JOptionPane.showMessageDialog(this,"Number of extra eggs cannot be negative","Invalid Value",JOptionPane.ERROR_MESSAGE);
                txtExtra_eggs.requestFocus();
                txtExtra_eggs.selectAll();
                return;
            }
            
            /*calculate the total*/
            int total_eggs = (number_of_trays * 30) + extra_eggs;
            txtTotal.setText(String.valueOf(total_eggs));
            Connection con = null;
            PreparedStatement pstmt = null;
            
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                con = DriverManager.getConnection(cs, user, password);
               query = "INSERT INTO egg_collection (Poultry_house_ID, Collection_round, Number_of_trays, Extra_eggs, total_of_eggs) VALUES (?, ?, ?, ?, ?)";
                pstmt = con.prepareStatement(query);
                int poultryhouse_id = Integer.parseInt(s3);
                pstmt.setInt(1, poultryhouse_id);
                pstmt.setInt(2, collection_round);
                pstmt.setInt(3, number_of_trays);
                pstmt.setInt(4, extra_eggs);
                pstmt.setInt(5, total_eggs);
                
                /*validate saving*/
                int confirm = JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to Save this record","Confirm Save",JOptionPane.YES_NO_OPTION);
        
                if (confirm != JOptionPane.YES_OPTION) {
                    return; // User cancelled1
                }
                
                int rowsaffected = pstmt.executeUpdate();
                if(rowsaffected > 0){
                    JOptionPane.showMessageDialog(null,"Record Saved Successfully");
                    txt_ID.setText("");
                    txtExtra_eggs.setText("");
                    txtNumber_of_trays.setText("");
                    txtCollection_round.setText("");
                    comboPoultry_house.setSelectedIndex(-1);
                    txtTotal.setText("");

                    txtCollection_round.requestFocus();
                }
                else {
                    JOptionPane.showMessageDialog(this, "Failed to save record","Error",JOptionPane.ERROR_MESSAGE);
                }
}
          
          catch (SQLException ex){
              ex.printStackTrace();
}
          catch (ClassNotFoundException e){
              e.printStackTrace();
}
          finally {
                try{
                    if (st != null){
                        st.close();
                    }
                    if (con != null){
                        con.close();
                    }
                }
                catch (SQLException ex){
                    ex.printStackTrace();
                }
}
    }//GEN-LAST:event_btnSaveActionPerformed

