        String sql = "SELECT " +
        "b.Location AS Branch_Location, " +
        "b.Number_of_poultry_houses, " +
        "YEAR(fc.Entry_Date) AS Year, " +
        "MONTH(fc.Entry_Date) AS Month, " +
        "COUNT(fc.ID) AS Number_of_Entries, " +
        "SUM(fm.Total_Quantity) AS Total_Mixture_Produced, " +
        "SUM(fc.Total_Quantity) AS Total_Initial_Stock, " +
        "SUM(fc.Quantity_Consumed) AS Total_Consumed, " +
        "SUM(fc.Balance_in_stock) AS Ending_Stock,  " +
        "ROUND((SUM(fc.Quantity_Consumed) / SUM(fc.Total_Quantity)) * 100, 2) AS Consumption_Rate " +
        "FROM feed_consumption fc " +
        "INNER JOIN feed_mixture fm ON fc.Feed_Mixture_ID = fm.ID " +
        "INNER JOIN branches b ON fm.Branch_ID = b.ID " +
        "WHERE b.Location = 'NSUUBE' " +
        "    AND YEAR(fc.Entry_Date) = 2026 " +
        "    AND MONTH(fc.Entry_Date) = 1 " +
        "GROUP BY b.Location, b.Number_of_poultry_houses, YEAR(fc.Entry_Date), MONTH(fc.Entry_Date); ";

         ReportGenerator.generateReport("Namumira Monthly Feed Consumption", sql);
