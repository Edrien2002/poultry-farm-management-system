    private void jMenuItem52ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem52ActionPerformed
        String sql = "SELECT " +
        "ph.Name_of_house AS Poultry_House, " +
        "COUNT(ec.ID) AS Number_of_Collections, " +
        "SUM(ec.Total_of_eggs) AS Total_Eggs_Produced, " +
        "ROUND(AVG(ec.Total_of_eggs), 2) AS Avg_Eggs_Per_Collection, " +
        "MAX(ec.Total_of_eggs) AS Highest_Collection, " +
        "MIN(ec.Total_of_eggs) AS Lowest_Collection, " +
        "ROUND((SUM(ec.Total_of_eggs) / (SELECT SUM(Total_of_eggs) " +
        "                                   FROM egg_collection ec2 " +
        "                                   INNER JOIN poultry_house ph2 ON ec2.Poultry_house_ID = ph2.ID " +
        "                                   INNER JOIN branches b2 ON ph2.Branch_ID = b2.ID " +
        "                                   WHERE b2.Location = 'NSUUBE')) * 100, 2) AS Percentage_of_Branch_Total " +
        "FROM egg_collection ec " +
        "INNER JOIN poultry_house ph ON ec.Poultry_house_ID = ph.ID " +
        "INNER JOIN branches b ON ph.Branch_ID = b.ID " +
        "WHERE b.Location = 'NAMUMIRA' " +
        "    AND YEAR(ec.Date_Time) = 2026 " +
        "    AND MONTH(ec.Date_Time) = 1 " +
        "GROUP BY ph.Name_of_house " +
        "ORDER BY Total_Eggs_Produced DESC";
        
        ReportGenerator.generateReport("PULTRY HOUSE PERFORMANCE IN EGG COLLECTION WITHIN NAMUMIRA", sql);

    }//GEN-LAST:event_jMenuItem52ActionPerformed

